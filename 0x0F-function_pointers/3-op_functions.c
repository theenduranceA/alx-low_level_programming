#include "3-calc.h"

/**
 * op_add - Addition of 2 integers.
 * @a: The first number.
 * @b: The second number.
 * Return: The sum of a & b.
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Subtraction of 2 integers.
 * @a: The first number.
 * @b: The second number.
 * Return: The difference of a & b.
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Multiplication of 2 integers.
 * @a: The first number.
 * @b: The second number.
 * Return: The product of a & b.
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Division of 2 integers.
 * @a: The first number.
 * @b: The second number.
 * Return: Result of the division.
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - The modulus of 2 integers.
 * @a: The first number.
 * @b: The second number.
 * Return: The remainder.
 */

int op_mod(int a, int b)
{
	return (a % b);
}

